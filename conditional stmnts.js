// SWITCH stmt:

let a="F"
switch(a){
    case("A"):
    console.log("APPLE");
    break

    case("B"):
    console.log("BALL");
    break

    case("C"):
    console.log("CAT");
    break

    case("D"):
    console.log("DOG");
    break

    case("E"):
    console.log("ELEPHANT");
    break

    case("F"):
    console.log("FROG");
    break

    default:
    console.log("INVALID");
    break

}